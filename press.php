	<p>&ldquo;If the stated ambition of UCSB&rsquo;s Primavera festival, organized by Jeremy Haladyna, is to find junctures between the worlds of different musical ideas, media and machinery, the Friday night of the festival lived up to the mission. By contrast with conventional musical settings encountered in classical music, the music leaned into the margins, where fresh ideas and contexts are formed and experimented with.&rdquo;</p>
